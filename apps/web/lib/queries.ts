type RawBusinessRow = {
  id: string;
  name: string;
  slug: string;
  citySlug: string;
  categorySlug: string;
  status: string;
  starRating: string | null;
  reviewCount: string | null;
  cpsScore: string | null;
  localLoopScore: string | null;
  phone: string | null;
  addressStreet: string | null;
  addressCity: string | null;
  addressState: string | null;
  addressPostalCode: string | null;
};

function shapeBusinessRow(row: RawBusinessRow): DirectoryBusinessRow {
  return {
    id: row.id,
    name: row.name,
    slug: row.slug,
    citySlug: row.citySlug,
    categorySlug: row.categorySlug,
    status: row.status,
    starRating: toNumberOrNull(row.starRating),
    reviewCount: toNumberOrZero(row.reviewCount),
    cpsScore: toNumberOrZero(row.cpsScore),
    localLoopScore: toNumberOrZero(row.localLoopScore),
    phone: row.phone,
    addressStreet: row.addressStreet,
    addressCity: row.addressCity,
    addressState: row.addressState,
    addressPostalCode: row.addressPostalCode,
  };
}

